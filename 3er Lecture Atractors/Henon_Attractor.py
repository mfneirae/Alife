#******************************************************************************
#                      Logistic Map - Fix Point
#******************************************************************************
import matplotlib.pyplot as plt
import numpy as np
#******************************************************************************
#Designer Parameters
#******************************************************************************
resolution = 1000                    #Number of Points
a = 1.4
b = 0.3
#******************************************************************************
#Definitions
#******************************************************************************
x1 =[0.13]
y1 =[0.13]
#******************************************************************************
#Main
#******************************************************************************
for z in range(0,resolution):
    f1 = 1-a*pow(x1[z],2)+y1[z]
    f2 = b*x1[z]
    x1.append(f1)
    y1.append(f2)
#******************************************************************************
#Plot
#******************************************************************************
plt.grid(True)
plt.scatter(x1,y1, color="red")
plt.show()
